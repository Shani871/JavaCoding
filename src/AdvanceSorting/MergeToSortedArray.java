package AdvanceSorting;

public class MergeToSortedArray {
    public static void print(int[]arr){
        for(int els:arr){
            System.out.print(els+" ");
        }
        System.out.println();
    }
    public static void main(String[] args) {
        int[]arr ={10,41,25,63,14,125,256,};
        int []brr = {74,5,4,36,36,14,25};
        print(arr);
        print(brr);
    }
}
